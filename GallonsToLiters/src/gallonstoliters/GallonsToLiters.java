public class GalsToLits {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        double gallons;
        double liters;
        
        gallons = 10;
        liters = gallons * 3.8754;
        
        System.out.println(gallons + " gallons is " + liters + " liters.");
    }
}
